alert('Olá :)!!! Este programa calcula o quociente e o resto')

let primeiroNumeroInteiro = Number(prompt('Digite o primeiro número inteiro'))
let segundoNumeroInteiro = Number(prompt('Digite o segundo número inteiro'))

let quociente = primeiroNumeroInteiro / segundoNumeroInteiro
let resto = primeiroNumeroInteiro % segundoNumeroInteiro

alert('O quociente entre o primeiro e o segundo número inteiro é:' + quociente + 'e o resto é:' + resto)